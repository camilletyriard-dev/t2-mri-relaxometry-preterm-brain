def plot_tissue_fits_3panel(ds, tissues=None):
    """Linear, log, and residual panels for OLS/WLS/NLLS on tissue-mean curves."""
    from models import mono_exp, fit_log_linear, fit_nlls
    from models import fit_wls
    if tissues is None:
        tissues = [(1, 'CSF'), (2, 'GM'), (3, 'WM')]

    TE = np.asarray(ds['TE'], float)
    fig, axes = plt.subplots(len(tissues), 3, figsize=(14, 4 * len(tissues)))

    for i, (seg_idx, lab) in enumerate(tissues):
        curve = mean_decay_curve(ds, seg_idx)
        S0_ols, T2_ols = fit_log_linear(TE, curve)
        S0_wls, T2_wls = fit_wls(TE, curve)
        S0_nll, T2_nll = fit_nlls(TE, curve)
        TE_fine = np.linspace(TE.min(), TE.max(), 200)

        # Linear
        ax = axes[i, 0]
        ax.plot(TE, curve, 'ko', ms=6, label='data')
        ax.plot(TE_fine, mono_exp(TE_fine, S0_ols, T2_ols), 'b--',
                label=f'OLS T2={T2_ols:.1f}')
        ax.plot(TE_fine, mono_exp(TE_fine, S0_wls, T2_wls), 'g:',
                label=f'WLS T2={T2_wls:.1f}')
        ax.plot(TE_fine, mono_exp(TE_fine, S0_nll, T2_nll), 'r-',
                label=f'NLLS T2={T2_nll:.1f}')
        ax.set(xlabel='TE (ms)', ylabel='signal', title=f'{lab}, linear')
        ax.legend(fontsize=8)

        # Log
        axes[i, 1].semilogy(TE, curve, 'ko', ms=6)
        axes[i, 1].semilogy(TE_fine, mono_exp(TE_fine, S0_ols, T2_ols), 'b--')
        axes[i, 1].semilogy(TE_fine, mono_exp(TE_fine, S0_wls, T2_wls), 'g:')
        axes[i, 1].semilogy(TE_fine, mono_exp(TE_fine, S0_nll, T2_nll), 'r-')
        axes[i, 1].set(xlabel='TE (ms)', ylabel='signal (log)',
                       title=f'{lab}, log scale')

        # Residuals
        axes[i, 2].axhline(0, color='k', lw=1)
        for S0, T2, fmt, lbl in [(S0_ols, T2_ols, 'bo--', 'OLS'),
                                   (S0_wls, T2_wls, 'go:', 'WLS'),
                                   (S0_nll, T2_nll, 'ro-', 'NLLS')]:
            axes[i, 2].plot(TE, (curve - mono_exp(TE, S0, T2)) / S0, fmt,
                            label=f'{lbl} res/$S_0$')
        axes[i, 2].set(xlabel='TE (ms)', ylabel='normalised residual',
                       title=f'{lab}, residuals')
        axes[i, 2].legend(fontsize=8)

    fig.suptitle(f'Epicure{ds["id"]}, tissue-mean fits', y=1.02)
    plt.tight_layout(); plt.show()


def plot_cohort_residuals(preterm, tissues=None):
    """Cohort NLLS residuals by tissue, EP vs FT colour-coded."""
    from models import mono_exp, fit_nlls
    if tissues is None:
        tissues = {1: 'CSF', 2: 'GM', 3: 'WM'}

    fig, axes = plt.subplots(1, len(tissues), figsize=(13, 4), sharey=True)
    for ax, (seg_idx, label) in zip(axes, tissues.items()):
        for ds in preterm:
            curve = mean_decay_curve(ds, seg_idx)
            S0, T2 = fit_nlls(ds['TE'], curve)
            if not np.isfinite(T2):
                continue
            residual = curve - mono_exp(ds['TE'], S0, T2)
            colour = 'firebrick' if ds['EP'] else 'steelblue'
            ax.plot(ds['TE'], residual / S0, 'o-', lw=0.9, alpha=0.6,
                    color=colour)
        ax.axhline(0, color='k', lw=0.8)
        ax.set(xlabel='TE (ms)', title=f'{label} — NLLS residuals')
        ax.grid(False)
    axes[0].set_ylabel('residual / $S_0$')
    plt.suptitle('Cohort NLLS residuals (red = EP, blue = FT)')
    plt.tight_layout(); plt.show()


