def plot_group_boxplots(df):
    """Whole-WM T2 and MWF boxplots with p-value annotation."""
    from analysis import ttest_summary, sig_marker
    fig, axes = plt.subplots(1, 2, figsize=(8, 4), dpi=150)

    for ax, col, ylab, title in [
        (axes[0], 'T2_WM', 'WM T2 (ms)', 'Whole-WM T2'),
        (axes[1], 'v_MWF', 'WM MWF (v)', 'Whole-WM MWF'),
    ]:
        ep = pd.to_numeric(df.loc[df['EP'] == True, col], errors='coerce').dropna()
        ft = pd.to_numeric(df.loc[df['EP'] == False, col], errors='coerce').dropna()
        ax.boxplot([ep, ft], positions=[1, 2], widths=0.5, patch_artist=True,
                   showfliers=False,
                   boxprops=dict(facecolor='white', edgecolor='blue', lw=1),
                   medianprops=dict(color='red', lw=1),
                   whiskerprops=dict(color='gray', ls='--', lw=1),
                   capprops=dict(color='gray', lw=1))

        res = ttest_summary(ep.values, ft.values)
        mark = sig_marker(res['p'])
        ymin, ymax = ax.get_ylim()
        y_bar = ymax + 0.04 * (ymax - ymin)
        ax.plot([1, 2], [y_bar, y_bar], 'k-', lw=1)
        ax.text(1.5, y_bar + 0.01 * (ymax - ymin),
                f'p = {res["p"]:.3f}{" " + mark if mark else " (ns)"}',
                ha='center', va='bottom', fontsize=9)
        ax.set_ylim(ymin, y_bar + 0.10 * (ymax - ymin))
        ax.set_title(title); ax.set_ylabel(ylab)
        ax.set_xticks([1, 2])
        ax.set_xticklabels([f'preterm (n={len(ep)})',
                            f'term (n={len(ft)})'], fontsize=9)
    plt.suptitle('Whole-WM comparison (* p<0.05, ns = not significant)')
    plt.tight_layout(); plt.show()


def plot_sex_stratified(df_merged):
    """Two-panel boxplot: EP vs FT within female / male."""
    from analysis import ttest_summary, sig_marker
    fig, axes = plt.subplots(1, 2, figsize=(9, 4), dpi=150)
    positions = [1, 2, 4, 5]

    for ax, col, title, ylab in [
        (axes[0], 'T2_WM', 'White matter T2', 'T2 (ms)'),
        (axes[1], 'v_MWF', 'White matter MWF', 'MWF (v)'),
    ]:
        data = []
        for sex_code in [0, 1]:
            for ep_flag in [True, False]:
                vals = pd.to_numeric(
                    df_merged[(df_merged['EP'] == ep_flag)
                              & (df_merged['male'] == sex_code)][col],
                    errors='coerce').dropna()
                data.append(vals)

        ax.boxplot(data, positions=positions, widths=0.65, patch_artist=True,
                   showfliers=False,
                   boxprops=dict(facecolor='white', edgecolor='blue', lw=1),
                   medianprops=dict(color='red', lw=1),
                   whiskerprops=dict(color='gray', ls='--'),
                   capprops=dict(color='gray'))

        # Annotate pairs
        for pos_pair, (ep_vals, ft_vals) in [([1, 2], [data[0], data[1]]),
                                              ([4, 5], [data[2], data[3]])]:
            res = ttest_summary(ep_vals.values, ft_vals.values)
            if res is None:
                continue
            ymin, ymax = ax.get_ylim()
            y_bar = ymax + 0.04 * (ymax - ymin)
            ax.plot(pos_pair, [y_bar, y_bar], 'k-', lw=1)
            mark = sig_marker(res['p'])
            ax.text(np.mean(pos_pair), y_bar,
                    f'p={res["p"]:.3f}{" " + mark if mark else " (ns)"}',
                    ha='center', va='bottom', fontsize=8)

        ax.set_title(title); ax.set_ylabel(ylab)
        ax.set_xticks(positions)
        ax.set_xticklabels(['PT', 'FT', 'PT', 'FT'], fontsize=9)
        ymin, ymax = ax.get_ylim()
        ax.text(1.5, ymin + 0.02 * (ymax - ymin), 'female', ha='center', fontsize=9)
        ax.text(4.5, ymin + 0.02 * (ymax - ymin), 'male', ha='center', fontsize=9)

    plt.suptitle('WM parameters by group × sex')
    plt.tight_layout(); plt.show()


def plot_iq_scatter(merged):
    """WM T2 and MWF vs FSIQ with per-group Spearman annotations."""
    from scipy import stats as sp_stats
    from analysis import sig_marker
    fig, axes = plt.subplots(1, 2, figsize=(11, 4), dpi=150)

    for ax, col, ylab in [(axes[0], 'T2_WM', 'WM T2 (ms)'),
                          (axes[1], 'v_MWF', 'WM MWF (v)')]:
        for flag, c, lab in [(True, 'firebrick', 'EP'),
                             (False, 'steelblue', 'FT')]:
            sub = merged[merged['EP'] == flag]
            ax.scatter(sub['y19ps_fsiq_comp'], sub[col], s=22, alpha=0.7,
                       color=c, label=lab)

            sub_clean = sub.dropna(subset=[col, 'y19ps_fsiq_comp'])
            if len(sub_clean) >= 5:
                rho, p_rho = sp_stats.spearmanr(sub_clean['y19ps_fsiq_comp'],
                                                 sub_clean[col])
                mark = sig_marker(p_rho)
                y_pos = 0.97 if flag else 0.88
                ax.text(0.03, y_pos,
                        f'{lab}: ρ = {rho:+.3f}, p = {p_rho:.3f}'
                        f'{" " + mark if mark else " (ns)"}',
                        transform=ax.transAxes, ha='left', va='top',
                        fontsize=9, color=c,
                        bbox=dict(facecolor='white', edgecolor='none', alpha=0.85))

        ax.set(xlabel='FSIQ', ylabel=ylab)
        ax.legend(fontsize=8); ax.grid(alpha=0.3)
    plt.suptitle('WM parameters vs cognitive outcome (FSIQ)')
    plt.tight_layout(); plt.show()


def plot_regional_grid(df_reg, stats_df, wm_regions):
    """2×4 grid: T2 (top) and MWF (bottom) boxplots per region with p-values."""
    from analysis import ttest_summary, sig_marker
    bonf_thr = 0.05 / len(stats_df)

    fig, axes = plt.subplots(2, len(wm_regions), figsize=(14, 7),
                             sharey='row', dpi=150)

    for row_i, (metric, ylab) in enumerate([('T2', 'WM T2 (ms)'),
                                             ('v', 'WM MWF (v)')]):
        for col_i, rname in enumerate(wm_regions.values()):
            ax = axes[row_i, col_i]
            sub = df_reg[df_reg['region'] == rname]
            ep = pd.to_numeric(sub.loc[sub['EP'] == True, metric],
                               errors='coerce').dropna()
            ft = pd.to_numeric(sub.loc[sub['EP'] == False, metric],
                               errors='coerce').dropna()

            ax.boxplot([ep, ft], positions=[1, 2], widths=0.5,
                       patch_artist=True, showfliers=False,
                       boxprops=dict(facecolor='white', edgecolor='blue', lw=1),
                       medianprops=dict(color='red', lw=1),
                       whiskerprops=dict(color='gray', ls='--'),
                       capprops=dict(color='gray'))

            res = ttest_summary(ep.values, ft.values)
            if res:
                mark = sig_marker(res['p'], bonf_thr)
                ymin, ymax = ax.get_ylim()
                y_bar = ymax + 0.04 * (ymax - ymin)
                ax.plot([1, 2], [y_bar, y_bar], 'k-', lw=1)
                ax.text(1.5, y_bar,
                        f'p={res["p"]:.3f}{" " + mark if mark else " (ns)"}',
                        ha='center', va='bottom', fontsize=8)

            ax.set_title(rname, fontsize=10)
            ax.set_xticks([1, 2])
            ax.set_xticklabels([f'PT (n={len(ep)})', f'FT (n={len(ft)})'],
                               fontsize=8)
            if col_i == 0:
                ax.set_ylabel(ylab)

    plt.suptitle('Regional WM analysis\n'
                 f'* uncorrected p<0.05  ** Bonferroni p<0.05 (α={bonf_thr:.4f})')
    plt.tight_layout(); plt.show()


