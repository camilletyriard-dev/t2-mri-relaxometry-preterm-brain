def plot_biexp_decomposition(ds, S0, v, T2s=20.0, T2l=80.0):
    """Linear + log panels showing the bi-exp short/long decomposition."""
    from models import bi_exp_fixed
    TE = np.asarray(ds['TE'], float)
    wm = mean_decay_curve(ds, 3)
    TE_fine = np.linspace(TE.min(), TE.max(), 300)

    fig, axes = plt.subplots(1, 2, figsize=(13, 4.5))
    sig_tot = bi_exp_fixed(TE_fine, S0, v, T2s, T2l)
    sig_sh = S0 * v * np.exp(-TE_fine / T2s)
    sig_lo = S0 * (1 - v) * np.exp(-TE_fine / T2l)

    axes[0].plot(TE, wm, 'ko', ms=6, label='WM data', zorder=5)
    axes[0].plot(TE_fine, sig_tot, 'r-', lw=2, label='total fit')
    axes[0].plot(TE_fine, sig_sh, 'b--', lw=1.5, label=f'short ({T2s:.0f} ms)')
    axes[0].plot(TE_fine, sig_lo, 'g--', lw=1.5, label=f'long ({T2l:.0f} ms)')
    axes[0].set(xlabel='TE (ms)', ylabel='signal', title='Linear')
    axes[0].legend(fontsize=8)

    axes[1].semilogy(TE, wm, 'ko', ms=6, zorder=5)
    axes[1].semilogy(TE_fine, sig_tot, 'r-', lw=2)
    axes[1].set(xlabel='TE (ms)', ylabel='signal (log)', title='Log')

    fig.suptitle(f'Epicure{ds["id"]} WM, v = {v:.3f}, S0 = {S0:.1f}', y=1.03)
    plt.tight_layout(); plt.show()


def plot_ci_intervals(df_ci, tissues=('CSF', 'GM', 'WM')):
    """Interval plot of v with 95% bootstrap CIs, coloured by EP/FT."""
    fig, axes = plt.subplots(1, len(tissues), figsize=(13, 4.5), sharey=False)
    for ax, tissue in zip(axes, tissues):
        sub = df_ci[df_ci['tissue'] == tissue].sort_values('v').reset_index(drop=True)
        y = np.arange(len(sub))
        colours = ['firebrick' if ep else 'steelblue' for ep in sub['EP']]
        for yi, row, c in zip(y, sub.itertuples(index=False), colours):
            ax.plot([row.CI_low, row.CI_high], [yi, yi], color=c, lw=1.2, alpha=0.7)
        ax.scatter(sub['v'], y, c=colours, s=18, alpha=0.9, zorder=3)
        ax.set_xlabel('v'); ax.set_title(tissue); ax.set_yticks([]); ax.grid(False)
    axes[0].set_ylabel('subjects ranked by v')
    plt.suptitle('Fixed-T2 v with 95% bootstrap CIs (red = EP, blue = FT)')
    plt.tight_layout(); plt.show()


def plot_ci_boxplots(df_ci, tissues=('CSF', 'GM', 'WM')):
    """Boxplots of v by tissue and EP/FT group."""
    fig, axes = plt.subplots(1, len(tissues), figsize=(10, 4), sharey=True)
    for ax, tissue in zip(axes, tissues):
        sub = df_ci[df_ci['tissue'] == tissue]
        vals_ft = sub.loc[sub['EP'] == False, 'v'].dropna().values
        vals_ep = sub.loc[sub['EP'] == True, 'v'].dropna().values
        ax.boxplot([vals_ft, vals_ep], labels=['FT', 'EP'], widths=0.45,
                   patch_artist=True, showfliers=True,
                   boxprops=dict(facecolor='white', edgecolor='blue', lw=1.2),
                   medianprops=dict(color='red', lw=1.2),
                   flierprops=dict(marker='+', markeredgecolor='red', ms=6))
        ax.set_title(tissue); ax.grid(False)
    axes[0].set_ylabel('v')
    plt.suptitle('Bootstrap v estimates by tissue and group', y=1.02)
    plt.tight_layout(); plt.show()


