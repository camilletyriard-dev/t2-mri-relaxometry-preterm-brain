def plot_echo_gallery(datasets, n=3):
    """Show all echo times for the first n subjects, one row each."""
    for ds in datasets[:n]:
        sl = ds['reg'].shape[2] // 2
        n_te = len(ds['TE'])
        fig, axes = plt.subplots(1, n_te, figsize=(2 * n_te, 4))
        vmax = np.percentile(ds['reg'][..., 0], 99)
        for ax, k in zip(axes, range(n_te)):
            ax.imshow(ds['reg'][:, :, sl, k].T, cmap='gray',
                      origin='lower', vmin=0, vmax=vmax)
            ax.set_title(f'TE = {ds["TE"][k]:.1f} ms')
            ax.axis('off')
        plt.suptitle(f'Epicure{ds["id"]}, central axial slice')
        plt.tight_layout(); plt.show()


def plot_seg_overlay_slices(datasets, n=2, n_slices=5):
    """Hard segmentation overlay across representative axial slices."""
    from quality import representative_brain_slices
    labels = ['Bottom', 'Lower-mid', 'Middle', 'Upper-mid', 'Top']
    cmap = _seg_cmap()

    for ds in datasets[:n]:
        slices = representative_brain_slices(ds['mask'], n_slices)
        vmax = np.percentile(ds['reg'][..., 0], 99)
        seg_h = hard_seg(ds)

        fig, axes = plt.subplots(1, n_slices, figsize=(18, 4.5))
        for ax, sl, lab in zip(axes, slices, labels):
            img = ds['reg'][:, :, sl, 0].T
            seg2d = np.where(ds['mask'][:, :, sl].T, seg_h[:, :, sl].T, 0)
            ax.imshow(img, cmap='gray', origin='lower', vmin=0, vmax=vmax)
            ax.imshow(seg2d, cmap=cmap, origin='lower', vmin=0, vmax=5)
            ax.set_title(f'{lab} (slice {sl})', fontsize=10)
            ax.axis('off')
        axes[0].legend(handles=_seg_legend(), loc='lower left', fontsize=8)
        plt.suptitle(f'Epicure{ds["id"]}, hard segmentation overlay')
        plt.tight_layout(); plt.show()


def plot_soft_seg_channels(ds):
    """Six-panel display of soft segmentation probability channels."""
    sl = ds['reg'].shape[2] // 2
    img = ds['reg'][:, :, sl, 0].T
    vmax = np.percentile(ds['reg'][..., 0], 99)
    mask2d = ds['mask'][:, :, sl].T

    fig, axes = plt.subplots(2, 3, figsize=(13, 8))
    for k, ax in enumerate(axes.ravel()):
        seg2d = np.where(mask2d, ds['seg'][:, :, sl, k].T, 0.0)
        ax.imshow(img, cmap='gray', origin='lower', vmin=0, vmax=vmax)
        rgba = np.zeros(seg2d.shape + (4,), dtype=float)
        rgba[..., :3] = to_rgba(SEG_COLOURS.get(k, '#888'))[:3]
        if seg2d.max() > 0:
            rgba[..., 3] = 0.75 * (seg2d / seg2d.max())
        ax.imshow(rgba, origin='lower')
        ax.set_title(f'{k}: {SEG_NAMES[k]}'); ax.axis('off')
    plt.suptitle(f'Epicure{ds["id"]}, soft segmentation channels')
    plt.tight_layout(); plt.show()


def plot_tissue_decay_curves(datasets, n=3):
    """Segmentation overlay + semilog tissue-mean decay curves, n subjects."""
    cmap = _seg_cmap()
    fig, axes = plt.subplots(n, 2, figsize=(12, 3.5 * n))
    if n == 1:
        axes = axes.reshape(1, 2)

    for row, ds in enumerate(datasets[:n]):
        sl = ds['reg'].shape[2] // 2
        seg_h = hard_seg(ds)
        img = ds['reg'][:, :, sl, 0].T
        vmax = np.percentile(ds['reg'][..., 0], 99)
        seg_show = np.where(ds['mask'][:, :, sl].T, seg_h[:, :, sl].T, 0)

        axes[row, 0].imshow(img, cmap='gray', origin='lower', vmin=0, vmax=vmax)
        axes[row, 0].imshow(seg_show, cmap=cmap, origin='lower', vmin=0, vmax=5)
        axes[row, 0].set_title(f'Epicure{ds["id"]}'); axes[row, 0].axis('off')

        for k in [1, 2, 3, 4, 5]:
            c = mean_decay_curve(ds, k)
            axes[row, 1].semilogy(ds['TE'], c, 'o-', color=SEG_COLOURS[k],
                                  lw=1.4, ms=4, label=SEG_NAMES[k])
        axes[row, 1].set_xlabel('TE (ms)')
        axes[row, 1].set_ylabel('mean signal (log)')
        axes[row, 1].set_title(f'Epicure{ds["id"]} — mean decay by tissue')
        axes[row, 1].grid(False)

    axes[0, 0].legend(handles=_seg_legend(), loc='lower left', fontsize=8)
    axes[0, 1].legend(fontsize=8)
    plt.tight_layout(); plt.show()


def plot_voxel_heterogeneity(ds, seg_idx=3, n_voxels=200, seed=0):
    """Semilog overlay of n random WM voxel decay curves."""
    from utils import region_signals
    wm = region_signals(ds, seg_idx=seg_idx)
    rng = np.random.default_rng(seed)
    idx = rng.choice(len(wm), size=min(n_voxels, len(wm)), replace=False)

    fig, ax = plt.subplots(figsize=(8, 4.2))
    for i, vi in enumerate(idx):
        curve_n = wm[vi] / max(wm[vi, 0], 1e-6)
        ax.semilogy(ds['TE'], curve_n, alpha=0.65, lw=1.1,
                    color=plt.cm.viridis(i / len(idx)))
    ax.set(xlabel='TE (ms)', ylabel='normalised signal (log)',
           title=f'Epicure{ds["id"]} — {len(idx)} random WM voxel decays')
    ax.grid(False); plt.tight_layout(); plt.show()


def plot_monotonicity_map(ds, viol_map):
    """Side-by-side: TE1 anatomy and monotonicity violation map."""
    sl = viol_map.shape[2] // 2
    fig, axes = plt.subplots(1, 2, figsize=(10, 4))
    axes[0].imshow(ds['reg'][:, :, sl, 0].T, cmap='gray', origin='lower')
    axes[0].set_title(f'Epicure{ds["id"]} — TE1'); axes[0].axis('off')

    img = viol_map[:, :, sl].copy()
    img[~ds['mask'][:, :, sl]] = np.nan
    cm = plt.get_cmap('Reds').copy(); cm.set_bad('white')
    im = axes[1].imshow(img.T, cmap=cm, origin='lower', vmin=0, vmax=0.4)
    plt.colorbar(im, ax=axes[1], label='violation fraction')
    axes[1].set_title('monotonicity violations'); axes[1].axis('off')
    plt.tight_layout(); plt.show()


def plot_snr_curves(preterm):
    """Per-subject SNR vs echo time, coloured by EP/FT."""
    from quality import estimate_sigma_bg
    fig, ax = plt.subplots(figsize=(8, 4.2))
    for ds in preterm:
        sigma = estimate_sigma_bg(ds['reg'], ds['mask'])
        mean_sig = np.array([ds['reg'][ds['mask'], k].mean()
                             for k in range(len(ds['TE']))])
        snr = mean_sig / sigma
        colour = 'firebrick' if ds['EP'] else 'steelblue'
        ax.plot(ds['TE'], snr, '-', lw=0.6, alpha=0.35, color=colour)
    ax.axhline(3, color='k', ls='--', lw=1, label='SNR = 3')
    ax.set(xlabel='TE (ms)', ylabel='mean in-brain SNR',
           title=f'Preterm cohort — per-echo mean SNR (n = {len(preterm)})\n'
                 'red = EP, blue = FT')
    ax.grid(True, alpha=0.3); ax.legend(fontsize=9)
    plt.tight_layout(); plt.show()


def plot_logfit_residuals(ds, seg_list=(1, 2, 3)):
    """Semilog decay + log-linear fit + residuals for CSF/GM/WM."""
    from quality import fit_logline_and_residuals
    fig, axes = plt.subplots(2, len(seg_list), figsize=(12, 6), sharex=True)

    for j, seg_idx in enumerate(seg_list):
        c = mean_decay_curve(ds, seg_idx)
        xfit, yfit, res = fit_logline_and_residuals(c, ds['TE'])
