def plot_nnls_plain_vs_reg(ds, a_plain, a_reg, mu_star, T2_grid):
    """Data fit and T2 spectrum: plain vs regularised NNLS."""
    from models import make_kernel
    TE = ds['TE']
    wm = mean_decay_curve(ds, 3)
    K = make_kernel(TE, T2_grid)

    fig, axes = plt.subplots(1, 2, figsize=(12, 4))
    axes[0].semilogy(TE, wm, 'ko', ms=6, label='data', zorder=5)
    axes[0].semilogy(TE, K @ a_plain, 'r-', lw=1.6, label='plain NNLS')
    axes[0].semilogy(TE, K @ a_reg, 'b--', lw=1.6,
                     label=f'regularised (μ={mu_star:.2g})')
    axes[0].set(xlabel='TE (ms)', ylabel='signal (log)',
                title=f'WM fit, Epicure{ds["id"]}')
    axes[0].legend(fontsize=8)

    axes[1].semilogx(T2_grid, a_plain, 'r-', lw=1.3, alpha=0.7, label='plain')
    axes[1].semilogx(T2_grid, a_reg, 'b-', lw=1.8, label='regularised')
    axes[1].axvspan(10, 40, color='grey', alpha=0.15, label='myelin (10–40 ms)')
    axes[1].set(xlabel='T2 (ms)', ylabel='amplitude', title='T2 spectrum')
    axes[1].legend(fontsize=8)
    plt.tight_layout(); plt.show()


def plot_cohort_spectra(spectra, T2_grid, tissues=None):
    """Per-tissue NNLS spectra across the cohort."""
    if tissues is None:
        tissues = {'CSF': 'royalblue', 'GM': 'forestgreen', 'WM': 'firebrick'}
    fig, axes = plt.subplots(1, len(tissues), figsize=(14, 4),
                             sharex=True, sharey=True)
    for ax, (t, col) in zip(axes, tissues.items()):
        mat = np.asarray(spectra[t])
        for row in mat:
            ax.semilogx(T2_grid, row, color='grey', alpha=0.15, lw=0.6)
        ax.semilogx(T2_grid, mat.mean(axis=0), color=col, lw=2, label='mean')
        ax.axvspan(10, 40, color='gold', alpha=0.15, label='myelin')
        ax.axvspan(50, 150, color='lightgreen', alpha=0.15, label='IE water')
        ax.axvspan(1000, 2000, color='lightblue', alpha=0.15, label='CSF')
        ax.set(xlabel='T2 (ms)', title=f'{t} (n = {mat.shape[0]})')
    axes[0].set_ylabel('amplitude'); axes[0].legend(fontsize=7)
    plt.suptitle('Preterm cohort NNLS T2 spectra')
    plt.tight_layout(); plt.show()


