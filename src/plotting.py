def plot_aicc_scatter(df_cmp, tissues=('WM', 'GM', 'CSF')):
    """Per-subject dAICc scatter, negative = 2-comp preferred."""
    fig, axes = plt.subplots(1, len(tissues), figsize=(13, 4), sharey=True)
    for ax, tissue in zip(axes, tissues):
        sub = df_cmp[df_cmp['tissue'] == tissue]
        colours = ['firebrick' if ep else 'steelblue' for ep in sub['EP']]
        ax.scatter(range(len(sub)), sub['dAICc'], c=colours, s=14, alpha=0.7)
        ax.axhline(0, color='k', lw=1)
        ax.axhline(-2, color='g', ls='--', lw=0.8, label='2-comp preferred')
        ax.axhline(+2, color='r', ls='--', lw=0.8, label='1-comp preferred')
        ax.set(xlabel='subject', title=tissue)
    axes[0].set_ylabel('dAICc (2-comp − 1-comp)')
    axes[0].legend(fontsize=8)
    plt.suptitle('AICc per subject (negative = 2-comp wins)')
    plt.tight_layout(); plt.show()


def plot_mcmc_vs_bootstrap(post, v_hat_m, ci_m, v_hat_b, ci_b, ds):
    """MCMC trace + posterior histogram vs bootstrap 95% CI."""
    lo_m, hi_m = ci_m
    lo_b, hi_b = ci_b
    fig, axes = plt.subplots(1, 2, figsize=(11, 4))

    axes[0].plot(post, lw=0.5, color='steelblue')
    axes[0].axhline(v_hat_m, color='red', lw=1, label=f'median = {v_hat_m:.3f}')
    axes[0].set(xlabel='iteration (post burn-in)', ylabel='v', title='MCMC trace')
    axes[0].legend(fontsize=8)

    axes[1].hist(post, bins=40, density=True, alpha=0.5, color='steelblue',
                 label='MCMC posterior')
    axes[1].axvspan(lo_b, hi_b, alpha=0.2, color='firebrick',
                    label=f'Boot 95% CI [{lo_b:.3f}, {hi_b:.3f}]')
    axes[1].axvline(v_hat_b, color='firebrick', lw=1.5, ls='--',
                    label=f'Boot v = {v_hat_b:.3f}')
    axes[1].set(xlabel='v', ylabel='density', title='Posterior vs bootstrap')
    axes[1].legend(fontsize=8)

    plt.suptitle(f'Parameter precision — Epicure{ds["id"]} WM')
    plt.tight_layout(); plt.show()


