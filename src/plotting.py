def _seg_cmap():
    """Transparent background + semi-transparent tissue colours."""
    return ListedColormap([
        (0, 0, 0, 0.0),
        to_rgba(SEG_COLOURS[1], 0.45),
        to_rgba(SEG_COLOURS[2], 0.45),
        to_rgba(SEG_COLOURS[3], 0.45),
        to_rgba(SEG_COLOURS[4], 0.45),
        to_rgba(SEG_COLOURS[5], 0.45),
    ])


def _seg_legend():
    """Patch handles for the tissue-class legend."""
    return [Patch(facecolor=SEG_COLOURS[k], edgecolor='none',
                  label=SEG_NAMES[k]) for k in [1, 2, 3, 4, 5]]


TISSUE_COLOURS = {'CSF': '#ffc800', 'GM': '#16766b', 'WM': '#945ad6',
                  'Deep GM': '#09413a', 'Brainstem': '#c44536'}


